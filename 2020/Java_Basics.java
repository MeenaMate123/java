// Somes rules related to identifiers are:
// Rule1. Identifiers are not allowed to starts with digit(0-9).

// Rule2. The only allowed characters in java identifiers are: a to z, A to Z, 0
// to 9, _(underscore), $.

// Rule3. We can't use reserved words as identifiers.

// Rule4. Predefined java class names and interface names we use as identifiers
// but not recommended to use.

// 2. Reserved words:
// some identifiers are reserved to associate some functionality

// Reserved words for data types: byte short int long float double char boolean

// Reserved words for flow control: if else switch case default for do while
// break continue return

// Keywords for modifiers: public private protected static final abstract
// synchronized native strictfp transient volatile

// Keywords for exception handling: try catch finally throw throws assert

// Class related keywords: class package import extends implements interface

// Object related keywords: new instanceof super this

// Void return type keyword

// Others: true false null enum

